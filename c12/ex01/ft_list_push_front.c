#include "ft_list.h"

void	ft_list_push_front(t_list **begin_list, void *data)
{
	t_list	*list;

	if (*begin_list)
	{
		list = ft_create_elem(data);
		list->next = *ft_create_elem;
		list = *begin_list;
	}
	else
		return (list->data = ft_create_elem(data));
}
