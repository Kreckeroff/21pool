int	ft_atoi(char *str)
{
	int flag;
	int num;

	flag = 0;
	num = 0;
	while ((*str == '\f') || (*str == '\n') || (*str == '\r')
			|| (*str == '\v') || (*str == '\t') || (*str == ' '))
		str++;
	while ((*str == '-') || (*str == '+'))
	{
		if (*str == '-')
			flag++;
		str++;
	}
	while ((*str >= '0') && (*str <= '9'))
	{
		num = (num * 10) + (*str - '0');
		str++;
	}
	if (flag % 2 == 0)
		return (num);
	else
		return (num * -1);
}
