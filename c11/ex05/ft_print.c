int	sum(int x, int y)
{
	return (x + y);
}

int	min(int x, int y)
{
	return (x - y);
}

int	mul(int x, int y)
{
	return (x * y);
}

int	del(int x, int y)
{
	return (x / y);
}

int	mod(int x, int y)
{
	return (x % y);
}
