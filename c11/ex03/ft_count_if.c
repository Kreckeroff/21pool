int	ft_count_if(char **tab, int length, int (*f)(char*))
{
	int count;
	int i;

	i = 0;
	count = 0;
	while (length > count)
	{
		if (f(*(tab + count)) != 0)
			i++;
		count++;
	}
	return (i);
}
